    public JFMain() {
        initComponents();
        this.setTitle(("Galactic War Vector Game - By Fernando Paniagua"));
        //Calcular dimensiones de la pantalla
        GraphicsDevice gd = GraphicsEnvironment.getLocalGraphicsEnvironment().getDefaultScreenDevice();
        XPos = (gd.getDisplayMode().getWidth()-SCREEN_WIDTH)/2;
        YPos = (gd.getDisplayMode().getHeight()-SCREEN_HEIGHT)/2;
        //Los par√°metros de los insets corrigen espacios de bordes, toolbar, etc.
        this.setBounds(this.XPos, this.YPos,
                JFMain.SCREEN_WIDTH + this.getInsets().left + this.getInsets().right,
                JFMain.SCREEN_HEIGHT + jmbBarraMenu.getHeight() + this.getInsets().bottom + this.getInsets().top);
        this.getContentPane().add("test", jpst);
        new Thread() {
            public void run() {
                while (true) {
                    jpst.repaint();
                    try {
                        sleep(7);
                        //sleep(500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(JFMain.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }.start();
    }

